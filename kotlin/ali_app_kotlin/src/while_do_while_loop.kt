fun main()
{
    var n = 1

    while(n <= 10)
    {
        println("n = $n")
        n++
    }

    var n1 = 101

    while(n1 <= 100)
    {
        println("n1 = $n1")
        n1++
    }

    var n2 = 101

    do
    {
        println("n2 = $n2")
        n2++
    }while(n2 <= 100)

    println("End app")
}